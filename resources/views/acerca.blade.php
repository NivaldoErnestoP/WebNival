        ¿Qué es un equipo Factory Refurbished y/o OEM?
        
         
        
        Los productos Factory Refurbished se diferencian de los demás productos reacondicionados porque son restaurados a las especificaciones exactas por la empresa manufacturera. Vienen con la ventaja de la garantía de fábrica. 
        
        Todos aquellos productos devueltos, incluso sin haber sido usados o aquellas devoluciones que los grandes mayoristas hacen a las compañías fabricantes, tienen que pasar por una inspección exhaustiva antes de volver a ser puestos en el mercado, con todos los accesorios y las mismas características que los artículos nuevos (son devueltos por defectos en el empaque causados en el envío, devueltos por ser unidades de demostración, etc.).
        
        Las compañías podrían decir que son nuevos y tanto estética como funcionalmente están como nuevos, pero por ley tienen que obligatoriamente decir que son Factory Refurbished. 
        
        La principal ventaja de este tipo de productos es que se pueden conseguir a un a un menor precio. Comprar productos Factory Refurbished es una práctica cada vez más común a nivel mundial.
        
        Estas compañías no pondrán en juego su nombre, por lo que las inspecciones son exhaustivas y solo devuelven al mercado con el sello de producto Factory Refurbished aquellos artículos que cumplan todos los estándares de calidad y que estén funcionales al 100%.
        
        Aquellos artículos que no puedan ser reacondicionados o que no pasen las pruebas de calidad serán reciclados y no se ponen a la venta.
        
        El más beneficiado de esta práctica será el usuario final quien podrá encontrar artículos como nuevos a un menor precio disfrutando de la misma calidad y garantías de los equipos que no son Factory Refurbished.
        
        
        Los productos OEM se diferencian de las versiones retail en que carecen del envoltorio de la marca, accesorios como cables/tornillos o no incluyen software. Muchos fabricantes de hardware industrial ofrecen esta opción para que los clientes integren los equipos en sus propios empaques, cajas o sistemas, o para reducir los costos de sus productos y entregar un producto más económico a sus clientes.  
        
